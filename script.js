let currentLang = 'th';

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'th' : 'en';
    document.documentElement.lang = currentLang;
    updateContent();
}

function updateContent() {
    const content = translations[currentLang];

    // Update navigation
    document.querySelectorAll('.nav-links a').forEach(link => {
        const key = link.getAttribute('href').replace('#', '');
        link.textContent = content.nav[key];
    });

    // Update hero section
    document.querySelector('.hero h1').textContent = content.hero.title;
    document.querySelector('.hero .subtitle').textContent = content.hero.subtitle;

    // Update vision section
    document.querySelector('#vision h2').textContent = content.vision.title;
    document.querySelector('.vision-text p').textContent = content.vision.description;
    document.querySelectorAll('.stat-label').forEach(label => {
        const key = Object.keys(content.vision.stats).find(key => 
            translations.en.vision.stats[key] === label.getAttribute('data-original') ||
            translations.th.vision.stats[key] === label.getAttribute('data-original')
        );
        if (key) {
            label.textContent = content.vision.stats[key];
        }
    });

    // Update about section
    document.querySelector('#about h2').textContent = content.about.title;
    document.querySelector('.about-content p').textContent = content.about.description;

    // Update products section
    document.querySelector('#products h2').textContent = content.products.title;
    const productCards = document.querySelectorAll('.product-card');
    
    // First product (Granola)
    productCards[0].querySelector('h3').textContent = content.products.granola.title;
    productCards[0].querySelector('p').textContent = content.products.granola.description;
    productCards[0].querySelectorAll('.product-features span').forEach((span, index) => {
        span.textContent = content.products.granola.features[index];
    });
    
    // Second product (Protein Blend)
    productCards[1].querySelector('h3').textContent = content.products.protein.title;
    productCards[1].querySelector('p').textContent = content.products.protein.description;
    productCards[1].querySelectorAll('.product-features span').forEach((span, index) => {
        span.textContent = content.products.protein.features[index];
    });

    // Update learn more buttons
    document.querySelectorAll('.btn-primary').forEach(btn => {
        btn.textContent = content.products.learnMore;
    });

    // Update SDG section
    document.querySelector('#sdg h2').textContent = content.sdg.title;
    document.querySelectorAll('.sdg-card').forEach(card => {
        const goalNumber = card.getAttribute('data-goal');
        card.querySelector('h3').textContent = content.sdg.goals[goalNumber].title;
        card.querySelector('p').textContent = content.sdg.goals[goalNumber].description;
    });

    // Update footer
    document.querySelectorAll('.footer-links a').forEach(link => {
        const key = link.getAttribute('href').replace('#', '');
        link.textContent = content.nav[key];
    });
    document.querySelector('.copyright p').textContent = content.copyright;
}

// Initialize language button
function initLanguageButton() {
    const button = document.createElement('button');
    button.id = 'langToggle';
    button.className = 'lang-toggle';
    button.onclick = toggleLanguage;
    button.textContent = 'EN/TH';
    
    const nav = document.querySelector('.nav-links');
    nav.appendChild(button);
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    initLanguageButton();
    
    // Store original stat labels for reference
    document.querySelectorAll('.stat-label').forEach(label => {
        label.setAttribute('data-original', label.textContent);
    });
    
    updateContent();
});